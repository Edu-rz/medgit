module.exports = (sequelize, Sequelize) => {
    const Administrator = sequelize.define("Administrator", {
      id: {
        type: Sequelize.INTEGER,
        autoIncrement: true,
        primaryKey: true,
      },
      email: {
        type: Sequelize.STRING,
        unique: true,
        allowNull: false,
      },
      password: {
        type: Sequelize.STRING,
        allowNull: false,
      },
    });
  
    return Administrator;
  };
  